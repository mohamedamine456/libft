#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *str;

	str = 0;
	while (*s != '\0')
	{
		if (*s == c)
			str = (char *)s;
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (str);
}
