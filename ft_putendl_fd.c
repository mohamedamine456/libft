#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int s_len;

	s_len = 0;
	if (s != 0)
	{
		while (s[s_len] != '\0')
			s_len++;
		write(fd, s, s_len);
	}
	write(fd, "\n", 1);
}
