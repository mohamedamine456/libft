#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int s_len;

	if (s != 0)
	{
		s_len = 0;
		while (s[s_len] != '\0')
			s_len++;
		write(fd, s, s_len);
	}
}
